# PYTHON DICTIONARIES

D={1:'one',
   2:'two',
   3:'three',
   4:'four',
   5:'five'
   };

print(D[1]);  # prints the corresponding value of 1 in dictionary D i.e. 'one'
print(D[5]);  # prints the corresponding value of 5 in dictionary D i.e. 'five'

print(D);  # prints the dictionary D
# OUTPUT: {1: 'one', 2: 'two', 3: 'three', 4: 'four', 5: 'five'}